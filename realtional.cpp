#include<iostream>
using namespace std;
int main()
{
	float a=12.33, b=12.56;
	cout<<"Greater than : "<<(a>b)<<endl;//0
	cout<<"Less than   : "<<(a<b)<<endl;//1
	cout<<"Greater than equal to : "<<(a>=b)<<endl;//0
	cout<<"Less than equal to : "<<(a<=b)<<endl;//1
	cout<<"Equal to : "<<(a==b)<<endl;//0
	cout<<"Not equal to : "<<(a!=b)<<endl;//1
	return 0;
}
