#include<iostream>
using namespace std;
int main()
{
	int num1=100,num2=60;
	cout<<"Addition : "<<num1+num2<<endl;//160
	cout<<"Subtraction : "<<num1-num2<<endl;//40
	cout<<"Multiplication : "<<num1*num2<<endl;//6000
	cout<<"Division : "<<(float)num1/num2<<endl;//1.66
	cout<<"Modulus : "<<num1%num2<<endl;//40
	return 0;
	
}
